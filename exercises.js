/* #doubleArray
 *
 * Takes in an array of numbers and returns an array with each element doubled
 *
 * @param {Array} // [1,2,3]
 * @return {Array} // [2,4,6]
 */
var doubleArray;

function doubleArray (arr){
  let newArr = [];
  for(let i = 0; i < arr.length; i++){
    newArr.push(arr[i]*2);
  }
  return newArr;
};

