/* #splitString
 *
 * Takes in a string and returns an array of each individual character in the string.
 *
 * @param {String}
 * @return {Array}
 */
var splitString;
function splitString(str){
  let newArr = [];
  for(let i = 0; i < str.length; i++){
    newArr.push(str.charAt(str.indexOf(str[i])));
  }
  return newArr;
}

